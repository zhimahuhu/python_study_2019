def to_morse(normal_str):
    morse_dic = {
        'a': '·-',
        'b': '-···',
        'c': '-·-·',
        'd': '-··',
        'e': '·',
        'f': '··-·',
        'g': '--·',
        'h': '····',
        'i': '··',
        'j': '·---',
        'k': '-·-',
        'l': '·-··',
        'm': '--',
        'n': '-·',
        'o': '---',
        'p': '·--·',
        'q': '--·-',
        'r': '·-·',
        's': '···',
        't': '-',
        'u': '··-',
        'v': '···-',
        'w': '·--',
        'x': '-··-',
        'y': '-·--',
        'z': '--··',
        '1': '·----',
        '2': '··---',
        '3': '···--',
        '4': '····-',
        '5': '·····',
        '6': '-····',
        '7': '--···',
        '8': '---··',
        '9': '----·',
        '0': '-----',
        '.': '·-·-·-',
        ',': '--··--',
        '?': '··--··',
        ':': '---···',
        ';': '-·-·-·',
        '-': '-····-',
        '/': '-··-·',
        '"': '·-··-·',
        "'": '·----·',
        '(': '-·--·',
        ')': '-·--·-',
        '=': '-···-',
        '+': '·-·-·',
        '$': '···-··-',
        '_': '··--·-',
    }
    result = ''
    for each_char in normal_str:
        each_char = each_char.lower()
        if each_char in morse_dic:
            result += morse_dic.get(each_char)
        else:
            result += ' '
        result += ' '
    return result


if __name__ == '__main__':
    tmp_result = to_morse('love')
